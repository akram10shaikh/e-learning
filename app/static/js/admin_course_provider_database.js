const rowsPerPage = 10;
let currentPage = 1;

function renderTable() {
    const tableBody = document.querySelector('#activeAccountsTable tbody');
    tableBody.innerHTML = '';

    const start = (currentPage - 1) * rowsPerPage;
    const end = Math.min(start + rowsPerPage, courses.length);

    for (let i = start; i < end; i++) {
        const row = document.createElement('tr');
        row.innerHTML = ` 
            <td>${courses[i].name}</td>
            <td>${courses[i].email}</td>
            <td>${courses[i].phonenum}</td>
            <td>${courses[i].month}</td>
            <td>${courses[i].no_of_enrollment}</td>
            <td class="actions">
                <p class="actions_item details">${courses[i].detail}</p>
                <p class="actions_item edit">${courses[i].edit}</p>
                <p class="actions_item activate">${courses[i].suspend}</p>
            </td>
        `;
        tableBody.appendChild(row);
    }

    // Add empty rows if fewer than 10 rows are present
    for (let i = end; i < start + rowsPerPage; i++) {
        const row = document.createElement('tr');
        row.innerHTML = `
            <td></td>
            <td></td>
            <td></td>
            <td></td>
            <td></td>
            <td class="actions"></td>
        `;
        tableBody.appendChild(row);
    }

    document.getElementById('currentPage').innerText = `Page ${currentPage} of ${Math.ceil(courses.length / rowsPerPage)}`;
}

